    <h1><small>Lokasi Pantai Lebih</small></h1>
    <h3><small>Jika anda tertarik untuk mengunjungi pantai yang memiliki pasir hitam legam ini maka anda bisa langsung datang ke desa lebih, tepatnya di kecamatan dan Kabupaten Gianyar Bali. Jika anda sedang mengunjungi beberapa tempat wisata di Gianyar seperti rekreasi Ayung rafting ataupun Goa gajah maka anda bisa mampir ke pantai ini. Selain itu, rute untuk menuju ke pantai ini sendiri cukup mudah dan bisa diakses melalui jalan raya By Pass Ida Bagus Mantra. Dengan rute yang berada di jalan raya inilah maka akan memudahkan Anda yang tidak membawa kendaraan pribadi. Sampai di Pantai Pandan langsung akan terpesona melihat keindahan pantai ini.</small></h3>
